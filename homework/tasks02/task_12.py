"""Задача 12: Петя и Катя – брат и сестра.
Петя – студент, а Катя – школьница.
Петя помогает Кате по математике.
Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их отгадать.
Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их произведение P.
Помогите Кате отгадать задуманные Петей числа."""

def find_numbers(S, P):
    for x in range(1, 1001):
        y = S - x
        if x * y == P:
            return x, y
    return None, None

# Введите сумму S и произведение P, которые вам дал Петя
S = int(input("Введите сумму S: "))
P = int(input("Введите произведение P: "))

x, y = find_numbers(S, P)

if x is not None:
    print(f"Первое число (X): {x}")
    print(f"Второе число (Y): {y}")
else:
    print("Невозможно найти числа X и Y для данных подсказок.")